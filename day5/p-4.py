# negetive indexing

my_list = [10, 20, 30, 40, 50]
print(my_list[-1])  # Access the last element (50)
print(my_list[-2])  # Access the second last element (40)
print(my_list[-3])  # Access the third last element (30)


# slicing 
print(my_list[0:3])  # Access elements from index 0 to 2 (10, 20, 30)