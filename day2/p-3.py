# Shortened code for arithmetic operations with assignment operators

i = 4
i += 2  # i = i + 2
print(i)  # 6

i -= 2  # i = i - 2
print(i)  # 4

i *= 2  # i = i * 2
print(i)  # 8

i /= 2  # i = i / 2
print(i)  # 4.0

i //= 2  # i = i // 2
print(i)  # 2.0

i **= 2  # i = i ** 2
print(i)  # 4.0

i %= 2  # i = i % 2
print(i)  # 0.0

